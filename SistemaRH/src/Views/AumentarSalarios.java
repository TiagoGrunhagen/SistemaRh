    private void btnAumentarDepartamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAumentarDepartamentoActionPerformed
        FuncionarioDAO dao = new FuncionarioDAO();
        String departamento = String.valueOf(comboDepartamento.getSelectedItem());
        double multiplicador = Double.parseDouble((String) comboMultiplicadorDepartamento.getSelectedItem());
        for (Funcionario funcionario: dao.listar()){
            if (funcionario.getDepartamento().getNome().equals(departamento)){
                funcionario.setSalario(funcionario.getSalario() + multiplicador * funcionario.getSalario());
                dao.atualizar(funcionario);
            }
        }
        JOptionPane.showMessageDialog(null, "Aumento aplicado!");
    }//GEN-LAST:event_btnAumentarDepartamentoActionPerformed

    private void btnAumentarNivelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAumentarNivelActionPerformed
        FuncionarioDAO dao = new FuncionarioDAO();
        int nivel = Integer.parseInt(String.valueOf(comboNivel.getSelectedItem()));
        double multiplicador = Double.parseDouble((String) comboMultiplicadorNivel.getSelectedItem());
        for (Funcionario funcionario: dao.listar()){
            if (funcionario.getNivel() == nivel){
                funcionario.setSalario(funcionario.getSalario() + multiplicador * funcionario.getSalario());
                dao.atualizar(funcionario);
            }
        }
        JOptionPane.showMessageDialog(null, "Aumento aplicado!");
    }//GEN-LAST:event_btnAumentarNivelActionPerformed

