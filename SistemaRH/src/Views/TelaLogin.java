    private void EntrarLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EntrarLoginActionPerformed
        if (inputCpf.getText().length() < 14 || inputSenha.getText().length() < 1){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
            return;
        }
        Funcionario func = new Funcionario();
        if (func.autentica(inputSenha.getText(), inputCpf.getText(), String.valueOf(comboSistema.getSelectedItem()))){
            if (String.valueOf(comboSistema.getSelectedItem()).equals("Sistema RH")){
                TelaPrincipal tela = new TelaPrincipal();
                tela.setVisible(true);
            } else {
                SistemaGenerico tela = new SistemaGenerico(String.valueOf(comboSistema.getSelectedItem()));
                tela.setVisible(true);
            }
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Dados informados invÃ¡lidos!");
        }
    }//GEN-LAST:event_EntrarLoginActionPerformed

