    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (dadosValidos()){
            Departamento departamento = tableModel.buscaDepartamento(tableDepartamentos.getSelectedRow());
            Funcionario funcionario = new Funcionario(inputSenha.getText(), Double.parseDouble(inputSalario.getText()), Integer.parseInt((String) comboNivel.getSelectedItem()), departamento, inputNome.getText(), inputSobrenome.getText(), inputRg.getText(), inputCpf.getText(), inputTelefone.getText());
            funcionario.setId(Integer.parseInt(inputId.getText()));
            FuncionarioDAO dao = new FuncionarioDAO();
            if (dao.atualizar(funcionario)){
                JOptionPane.showMessageDialog(null, "Funcionario atualizado com sucesso.");
                this.dispose();
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

