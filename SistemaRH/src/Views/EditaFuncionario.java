    private boolean dadosValidos(){
        if (inputNome.getText().length() == 0){
            JOptionPane.showMessageDialog(null, "Insira o nome");
            return false;
        }
        if (inputSobrenome.getText().length() == 0){
            JOptionPane.showMessageDialog(null, "Insira o sobrenome");
            return false;
        }
        if (inputRg.getText().length() == 0){
            JOptionPane.showMessageDialog(null, "Insira o RG");
            return false;
        }
        if (inputCpf.getText().replaceAll("[-. ]", "").length() < 11){
            JOptionPane.showMessageDialog(null, "Insira o CPF");
            return false;
        }
        if (inputTelefone.getText().replaceAll("[-. ]", "").length() < 10){
            JOptionPane.showMessageDialog(null, "Insira o telefone");
            return false;
        }
        if (tableDepartamentos.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(null, "Selecione um departamento");
            return false;
        }
        if (inputSalario.getText().equals("") || Double.parseDouble(inputSalario.getText()) < 1000){
            JOptionPane.showMessageDialog(null, "O salario mínimo é 1000 reais.");
            return false;
        }
        return true;
    }

